def audit(session, event_type, session_id, branch_label, route_name, outcome, details):
    session["audit_tail"].append({
        "timestamp": ts(),
        "event_type": event_type,
        "project_id": PROJECT_ID,
        "session_id": session_id,
        "branch_label": branch_label,
        "route_name": route_name,
        "outcome": outcome,
        "details": details,
    })
    session["audit_tail"] = session["audit_tail"][-8:]

def save_session(project_dir: Path, session: dict):
    filename = f"{session['session_id']}.json"
    path = project_dir / filename
    write_json(path, session)
    return path

