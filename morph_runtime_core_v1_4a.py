def session_tree(project_dir: Path):
    ids = [p.stem for p in sorted(project_dir.glob("*.json"))]
    mapping = {}
    for sid in ids:
        data = read_json(project_dir / f"{sid}.json")
        mapping[sid] = {
            "parent_session_id": data.get("parent_session_id"),
            "branch_label": data.get("branch_label"),
            "children": [],
            "merge_parents": data.get("merge_parents", []),
            "merge_note": data.get("merge_note"),
        }
    for sid, info in mapping.items():
        parent = info["parent_session_id"]
        if parent and parent in mapping:
            mapping[parent]["children"].append(sid)
    roots = [sid for sid, info in mapping.items() if info["parent_session_id"] is None]
    return {"sessions": mapping, "roots": roots}

def integrity_report(project_dir: Path):
    problems = []
    files = sorted(project_dir.glob("*.json"))
    ids = set(p.stem for p in files)
    for p in files:
        data = read_json(p)
        parent = data.get("parent_session_id")
        if parent and parent not in ids:
            problems.append({"session_id": p.stem, "missing_parent": parent})
    report = {
        "checked_at": ts(),
        "project_dir": str(project_dir),
        "session_count": len(files),
        "problem_count": len(problems),
        "ok": len(problems) == 0,
        "problems": problems,
        "session_ids": sorted(ids),
    }
    write_json(project_dir / "integrity_report.json", report)
    return report

